package com.wutianyi.duowan.dataobject;

public class CodeActivityDO {
	private int gameId;
	private int actState;
	private String oneSentence;
	private int gotCount;
	private int hadBook;
	private int taoCount;
	public int getGameId() {
		return gameId;
	}
	public void setGameId(int gameId) {
		this.gameId = gameId;
	}
	public int getActState() {
		return actState;
	}
	public void setActState(int actState) {
		this.actState = actState;
	}
	public String getOneSentence() {
		return oneSentence;
	}
	public void setOneSentence(String oneSentence) {
		this.oneSentence = oneSentence;
	}
	public int getGotCount() {
		return gotCount;
	}
	public void setGotCount(int gotCount) {
		this.gotCount = gotCount;
	}
	public int getHadBook() {
		return hadBook;
	}
	public void setHadBook(int hadBook) {
		this.hadBook = hadBook;
	}
	public int getTaoCount() {
		return taoCount;
	}
	public void setTaoCount(int taoCount) {
		this.taoCount = taoCount;
	}
	
	
}
